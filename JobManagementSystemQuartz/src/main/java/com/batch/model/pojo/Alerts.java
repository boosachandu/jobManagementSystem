package com.batch.model.pojo;

/**
 * @author hjadhav1
 *
 */
public class Alerts {
	public String alertId;
	public String description;
	public int alertAge;
	public String alertOwner;
	public String relatedCaseId;
	/**
	 * @return the alertId
	 */
	public String getAlertId() {
		return alertId;
	}
	/**
	 * @param alertId the alertId to set
	 */
	public void setAlertId(String alertId) {
		this.alertId = alertId;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the alertAge
	 */
	public int getAlertAge() {
		return alertAge;
	}
	/**
	 * @param alertAge the alertAge to set
	 */
	public void setAlertAge(int alertAge) {
		this.alertAge = alertAge;
	}
	/**
	 * @return the alertOwner
	 */
	public String getAlertOwner() {
		return alertOwner;
	}
	/**
	 * @param alertOwner the alertOwner to set
	 */
	public void setAlertOwner(String alertOwner) {
		this.alertOwner = alertOwner;
	}
	/**
	 * @return the relatedCaseId
	 */
	public String getRelatedCaseId() {
		return relatedCaseId;
	}
	/**
	 * @param relatedCaseId the relatedCaseId to set
	 */
	public void setRelatedCaseId(String relatedCaseId) {
		this.relatedCaseId = relatedCaseId;
	}
	
	

}
